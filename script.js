let formulario = document.querySelector('#link')
formulario.addEventListener('mouseover', ()=>{
    formulario.style.width = '600px'
    formulario.style.color = 'white'
    formulario.style.transition = 'all 1s'
})
formulario.addEventListener('mouseout', ()=>{
    formulario.style.width = '100%'
    formulario.style.color = 'pink'
    formulario.style.transition = 'all 1s'
})



